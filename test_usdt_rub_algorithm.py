def test_explicit_v_from_percent_formula_is_preserved() -> None:
    source = _clean(
        """
        Томск
        78.18-0.668%=77.511869
        1445*77.51=112001.95
        """
    )
    expected = _clean(
        """
        /юсдт 1445 группа: Томск
        Город: Томск

        78.18-0.668%=77.511869
        1445*77.51=112001.95

        проверка:
        112001.95/1445=77.51
        """
    )
    assert format_transaction(source) == expected


def test_resale_branch_recovers_b_from_local_rate_only() -> None:
    source = _clean(
        """
        Мы:
        2744500/79.5=34522.0126
        Нам:78.4
        2744500/79=34740.506
        """
    )
