def test_resale_recovers_b_from_local_rate_inside_branch() -> None:
    source = _clean(
        """
        Нам:
        248000/79=3139,241

        Мы:78.17
        248000/79,108=3134,955
        """
    )
    expected = _clean(
        """
        /юсдт 3139-3135 группа: нет данных
        Город: нет данных

        Нам:
        248000/79=3139.241

        Мы:
        78.17+0.938=79.108
        248000/79.108=3134.955

        Проверка
        3139.241-3134.955=4.286
        """
    )
    assert format_transaction(source) == expected


